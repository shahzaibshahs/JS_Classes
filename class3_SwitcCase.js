// Switch Case

let fruit = "mango";

switch (fruit) {
    case "apple":
    console.log("Fruit is Apple");
    break;

    case "banana":
    console.log("Fruit is Banana");
    break;

    case "graphes":
    console.log("Fruit is Graphes");
    break;

    case "mango":
    console.log("Fruit is Mango");  
    break;

    default:
   console.log("Fruit is something else");
}