// Math Methods


//Math ceil - yeh function kisi bi float ke qarebi number ko reurn krta ha
var a = Math.ceil(5.3);

document.write(a,"<br>");

// floor - yeh hamey downword value de ga 


var a = Math.floor(7.3);

document.write(a,"<br>");  // jab hamey negative alue chaiye hogi to ha minus lagaye gy

// round - It returns closest integer value of the given number.

var a = Math.floor(7.3);

document.write(a,"<br>"); 

// trunc() - It returns an integer part of the given number.

var a = Math.trunc(73.00);

document.write(a,"<br>"); 

// max - yeh sabse bara number return krta ha given value me se

var a = Math.max(2,4,6,7,8,9);

document.write(a,"<br>"); 

// min - yeh sabse chota number return krta ha given value me se

var a = Math.min(2,4,6,7,8,9);

document.write(a,"<br>"); 

// sqrt - ye root nilaney ke liye hota ha

var a = Math.sqrt(49);

document.write(a,"<br>");

// cbrt means cubic root 

var a = Math.cbrt(100); // iskey ander kam kuch aysa hua hy 5X5X5X5 = 100

document.write(a,"<br>");

// pow - yeh kisi value ki power nikalny ke liye use hota ha

var a = Math.pow(3,4); // iskey ander kam kuch ayse hua ha 3 X 3 X 3 X = 81 - base ki value multiply hoti ha exponent sa

document.write(a,"<br>");

// random - yeh randomly numbers gernate kr ke deta ha

var a = Math.random();

document.write(a,"<br>");

// if we need random value btw 1 to 10 then

var a = Math.floor(Math.random() * 100);

document.write(a,"<br>");

// abs - isey jo value di jaye gi wohi repaet kr ke return kry ga

var a = Math.abs(37);

document.write(a,"<br>");

// pi - yeh pi ki value retunr krta ha

var a = Math.PI;

document.write(a,"<br>");